args <- commandArgs(trailingOnly = TRUE)
abstraction <- ifelse(length(args) >= 1, as.numeric(args[1]), 0.82)
performance_orientation <- ifelse(length(args) >= 2, as.numeric(args[2]), 0.96)
readability <- ifelse(length(args) >= 3, as.numeric(args[3]), 0.76)
formal_specification <- ifelse(length(args) >= 4, as.numeric(args[4]), 0.78)
ecosystem_depth <- ifelse(length(args) >= 5, as.numeric(args[5]), 0.92)
domain_fit <- ifelse(length(args) >= 6, as.numeric(args[6]), 0.98)
safety_orientation <- ifelse(length(args) >= 7, as.numeric(args[7]), 0.48)
institutional_adoption <- ifelse(length(args) >= 8, as.numeric(args[8]), 0.96)
historical_influence <- ifelse(length(args) >= 9, as.numeric(args[9]), 0.98)
governance_caution <- ifelse(length(args) >= 10, as.numeric(args[10]), 0.90)

score <- mean(c(abstraction, performance_orientation, readability, formal_specification, ecosystem_depth, domain_fit, safety_orientation, institutional_adoption, historical_influence, governance_caution))
cat(sprintf("history_score=%.6f\n", score))
