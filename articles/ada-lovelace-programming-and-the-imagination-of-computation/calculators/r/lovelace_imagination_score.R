score <- mean(c(programming_structure, symbolic_generality, machine_orientation, mathematical_grounding, imaginative_reach, limit_awareness, collaboration, authorship, modern_resonance, ai_caution))
cat(sprintf("imagination_score=%.6f\n", score))
